
public class Instructor {
	
	public Instructor(int instructorId, String firstName, String lastName, String information) {
		
		this.instructorId = instructorId;
		this.firstName = firstName;
		this.lastName = lastName;
		this.information = information;
	};
	
	
	int instructorId;
	String firstName;
	String lastName;
	String information; 
}
